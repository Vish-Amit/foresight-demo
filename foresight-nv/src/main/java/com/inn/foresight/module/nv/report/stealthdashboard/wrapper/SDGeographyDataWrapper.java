package com.inn.foresight.module.nv.report.stealthdashboard.wrapper;

public class SDGeographyDataWrapper {

	String geographyName;
	String noOfSamples;
	String avgValue;
	String totalSample;
	String percentage;

	public String getGeographyName() {
		return geographyName;
	}

	public void setGeographyName(String geographyName) {
		this.geographyName = geographyName;
	}

	public String getNoOfSamples() {
		return noOfSamples;
	}

	public void setNoOfSamples(String noOfSamples) {
		this.noOfSamples = noOfSamples;
	}

	public String getAvgValue() {
		return avgValue;
	}

	public void setAvgValue(String avgValue) {
		this.avgValue = avgValue;
	}

	public String getTotalSample() {
		return totalSample;
	}

	public void setTotalSample(String totalSample) {
		this.totalSample = totalSample;
	}

	public String getPercentage() {
		return percentage;
	}

	public void setPercentage(String percentage) {
		this.percentage = percentage;
	}
}
