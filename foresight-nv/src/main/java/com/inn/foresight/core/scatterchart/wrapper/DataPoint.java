package com.inn.foresight.core.scatterchart.wrapper;

/**
 * The Class DataPoint.
 */
public class DataPoint {


	/** The first KPI value. */
	private Double firstKPIValue;

	/** The second KPI value. */
	private Double secondKPIValue;

	/** The latitude. */
	private Double latitude;

	/** The longitude. */
	private Double longitude;

	/**
	 * Gets the latitude.
	 *
	 * @return the latitude
	 */
	public Double getLatitude() {
		return latitude;
	}

	/**
	 * Sets the latitude.
	 *
	 * @param latitude the new latitude
	 */
	public void setLatitude(Double latitude) {
		this.latitude = latitude;
	}

	/**
	 * Gets the longitude.
	 *
	 * @return the longitude
	 */
	public Double getLongitude() {
		return longitude;
	}

	/**
	 * Sets the longitude.
	 *
	 * @param longitude the new longitude
	 */
	public void setLongitude(Double longitude) {
		this.longitude = longitude;
	}

	/**
	 * Gets the second KPI value.
	 *
	 * @return the second KPI value
	 */
	public Double getSecondKPIValue() {
		return secondKPIValue;
	}

	/**
	 * Sets the second KPI value.
	 *
	 * @param secondKPIValue the new second KPI value
	 */
	public void setSecondKPIValue(Double secondKPIValue) {
		this.secondKPIValue = secondKPIValue;
	}

	/**
	 * Gets the first KPI value.
	 *
	 * @return the first KPI value
	 */
	public Double getFirstKPIValue() {
		return firstKPIValue;
	}

	/**
	 * Sets the first KPI value.
	 *
	 * @param firstKPIValue the new first KPI value
	 */
	public void setFirstKPIValue(Double firstKPIValue) {
		this.firstKPIValue = firstKPIValue;
	}

}
